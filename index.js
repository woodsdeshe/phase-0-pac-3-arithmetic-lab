function add(num1, num2){
    let res = num1 + num2
    return res
}

function subtract(num1, num2) {
    let res = num1 - num2
    return res
}

function multiply(num1, num2) {
    let res = num1 * num2
    return res
}

function divide(num1, num2) {
    let res = num1 / num2
    return res
}

function increment(n) {
   n++;
   return n
}
function decrement(n) {
    n--;
    return n
}

function makeInt(n) {
   return parseInt(n,10);
   
}

function preserveDecimal(n) {
   return parseFloat(n)
}